// Example of keyword usage.
// Three keywords are used: var, let and const.

//Variable declaration using var, let and const
var myName = "Katherine";
console.log(myName); // Output: Katherine

//let myAge = 30;
//const myHeight = 5.4;

//var, used for values that are subject to change or can be reassigned later in the code.

var myName = "Ife mi";
console.log(myName); 

let myAge = 31;
console.log(myAge);

const myHeight = 5.6;
console.log(myHeight);

// Variable assignment
