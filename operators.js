// Operators: Cn be Arithmetic, Comparison, Logical and Assignment.

//Arithmetic operators
let x = 10; // declaring the variables x and y
let y = 5;

let z = x + y; 
let a = x - y;
let b = x * y;
let c = x / y;
let d = x % y;
console.log(z); 
console.log(a);
console.log(b);
console.log(c);
console.log(d);

//Comparison operators
let e = x === y; // declaring that value of variable e is x equal to y;the value of e is either true or false depending on the result.
let f = x !== y; 
let g = x > y;
let h = x < y;
console.log(e); 
console.log(f);
console.log(g);
console.log(h);

//Assignment operators
let i = 50;
console.log(i);
let j = i += 5;
let l = i -= 10;
let m = i *= 2;
let n = i /= 5;
console.log(i);
console.log(j); 
console.log(l);
console.log(m);
console.log(n);

